package loops;

public class basicsOFloops {

	public static void main(String[] args) {
		// FOR LOOP
		for(int i = -7;i<12;i++) {
			System.out.println("sakshi");
		}
		
		//print 1 to 10
		for (int i = 1;i<=10;i++) {
			System.out.println(i);
		}
        int a ;
        for (a = 1;a<=10;a++) {
			System.out.println(a);
		}
		System.out.println(a);

	}

}
