package basic_java1;

public class DivisionOfDatatype {

	public static void main(String[] args) {
		double x = 5/2;
		System.out.println(x);
		//it gives o/p as 2.0 
		
		x = 5/2.0;
		System.out.println(x);
		
		x = 5.0/2;
		System.out.println(x);
		
		x =5.0/2.0;
		System.out.println(x);
		
		//all three gives o/p 2.5
	}

}
