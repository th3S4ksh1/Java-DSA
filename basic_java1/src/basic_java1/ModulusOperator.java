package basic_java1;

public class ModulusOperator {

	public static void main(String[] args) {
		//it returns the reminder
		
		int a = 2;
		int b = 5;
		System.out.println(a%b);
		
		a=5;
		b=-2;
		System.out.println(a%b);
		
		a=-39;
		b=10;
		System.out.println(a%b);
		
		a=-39;
		b=-10;
		System.out.println(a%b);
		
	}

}
