package IfElse;

public class RelationalOperators {

	public static void main(String[] args) {
		System.out.println(3==3);
		System.out.println(3!=3);
		System.out.println(1>=2);
		System.out.println(1<=2);
		System.out.println(1<2);
		System.out.println(1>2);
		
		boolean x = true;
		System.out.println(x);
		
		boolean y = false;
		System.out.println(y);
		
		boolean z = (3>2);
		System.out.println(z);
		

	}

}
